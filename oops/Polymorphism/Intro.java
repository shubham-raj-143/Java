package oops.Polymorphism;
/* Polymorphism : It allows us to perform a single action in different ways.
 * In other words, polymorphism allows us to define one interface and have multiple implementations.
 */

/* TYPES:
 * 1.> RUNTIME Polymorphism--: >>It is also known as Dynamic binding, Late Binding, and Overriding 
 * 								as well.
 * 							 
 * 				>>Overriding is run-time polymorphism having same method with same parameters or
 * 					Signatures, but associated in a class and its subclass.
 * 
 * 				>> Slower Execution
 * 
 * 
 * 2.> Compile Time Polymorphism--: >> It is also known as Static Binding, Early Binding, and 
 * 										Overloading as well
 * 
 * 							>> Overloading is a compile time Polymorphism where more than 1 methods
 * 							share the same name with different parameters or signature and different
 * 							return type
 * 
 * 							>> Faster Execution
 */

