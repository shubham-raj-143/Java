package oops.StaticKeyword;

/*We cannot declare a top-level class with static modifier but we can declare a nested class as static.
 * In Java we can define a class within another class.Such a class is called a NESTED class.
 * 
 * Nested class are of 2 types : Static and non Static  
 */
class Person {

	int age;
	String name;
	static String breed = "Homo Sapiens";
}